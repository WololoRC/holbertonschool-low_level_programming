#include <stdlib.h>
#include "calc.h"
/**
 *op_add - add numbers
 *
 *@a: num
 *@b: num
 * Return: add
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - sub nums
 *
 *@a: num
 *@b: num
 *
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - mul nums
 *
 *@a: num
 *@b: num
 *
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - div nums
 *
 *@a: num
 *@b: num
 *
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - mod nums
 *
 *@a: num
 *@b: num
 *
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
